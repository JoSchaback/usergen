// tests if the string p_s starts with the string p_word
bool isMatch(const char* p_s, const char* p_word) {
    int strLen_s    = strlen(p_s);
    int strLen_word = strlen(p_word);
    for( int k = 0; k < strLen_word; k++ ) {
        if( k >= strLen_s || p_s[k] != p_word[k] ) {
            return false;
        }
    }
    return true;
}

void printUsage();

int main(int argc, char *argv[]) {
    // default pattern. Can be overwritten by command line args
    const char* pattern = "AdjectiveNounNumber4";

    // number of user names to generate. Default is 1. Can be overwritten by command line args
    int numberOfUsernamesToGenerate = 1;

    bool srandInitialized = false;

    // parse command line args
	while (1) 
  	{
		int option_index = 0;
		static struct option long_options[] = 
		{
			{"count",   required_argument, NULL, 'c'},
			{"seed",    required_argument, NULL, 's'},
			{"help",    no_argument, NULL, 'h' },
			{"version", no_argument, NULL, 'v' },
			{NULL,      0,                 NULL,  0 }
		};

		int c = getopt_long(argc, argv, "-c:s:hv", long_options, &option_index);
		if (c == -1)
			break;

		switch (c) 
		{
        case 1:
            pattern = optarg;
            break;

		case 'c':
			numberOfUsernamesToGenerate = atoi(optarg);
			break;

		case 's':
    		srand(atoi(optarg));
            srandInitialized = true;
			break;

		case 'h':
			printUsage();
			return 0;

		case 'v':
			printf("version 1.0\n");
			break;

		case '?':
			printf("Unknown option %c\n", optopt);
			break;

		case ':':
			printf("Missing option for '%c'\n", optopt);
			break;

		default:
			printf("?? getopt returned character code 0%o, %c ??\n", c, c);
		}
	}

    // size of the arrays
    size_t adjectivesCount = sizeof(adjectives)/sizeof(adjectives[0]);
    size_t nounsCount      = sizeof(nouns)/sizeof(nouns[0]);    

    // we initiate the random number generator with the current time
    if(!srandInitialized) {
        srand(time(NULL));
    }

    // generate user names by counting up to the desired number of user names
    // In each iteration, scan the pattern and replace the placeholders with random words.
    for(int count = 1; count <= numberOfUsernamesToGenerate; count++) {
        const char* ptr = pattern;
        while( ptr[0] != '\0' ) {
            char buffer[100];
            if( isMatch(ptr, "Adjective") ) {
                strncpy(buffer, adjectives[rand() % adjectivesCount], sizeof(buffer)-1);
                buffer[0] = toupper(buffer[0]);
                printf("%s", buffer);
                ptr += sizeof("Adjective")-1;
            } else if( isMatch(ptr, "adjective") ) {
                strncpy(buffer, adjectives[rand() % adjectivesCount], sizeof(buffer)-1);
                printf("%s", buffer);
                ptr += sizeof("adjective")-1;
            } else if( isMatch(ptr, "Noun") ) {
                strncpy(buffer, nouns[rand()% nounsCount], sizeof(buffer)-1);
                buffer[0] = toupper(buffer[0]);
                printf("%s", buffer);
                ptr += sizeof("Noun")-1;
            } else if( isMatch(ptr, "noun") ) {
                strncpy(buffer, nouns[rand()% nounsCount], sizeof(buffer)-1);
                printf("%s", buffer);
                ptr += sizeof("noun")-1;
            } else if( isMatch(ptr, "Number") ) {
                ptr += sizeof("Number")-1;
                // extract digits after "Number"
                int digits = ptr[0] - 48; // '0' is ascii 48
                int r = rand() % ((int)pow(10, digits));
                printf("%d", r);
                ptr += sizeof(" ")-1;
            } else {
                printf("%c", ptr[0]);
                ptr++;
            }
        }
        printf("\n");
    }
    return 0;   
}

